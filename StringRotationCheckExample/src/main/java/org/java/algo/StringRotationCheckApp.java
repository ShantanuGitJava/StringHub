public class StringRotationCheckApp {

    /**
     * input : string,string || output : boolean || function : check 2nd string the
     * rotation of first one and return the boolean value
     * 
     * @param word
     * @param rotation
     * @return boolean
     */
    public static boolean checkRotation(String word, String rotation) {
	if (word.length() != rotation.length()) {
	    return false;
	}
	String TempWord = word + word;
	return TempWord.contains(rotation) ? true : false;

    }// checkRotation

}// StringRotationCheckApp
