public class ReverseWordOfSentenceApp {

    /**
     * input : String || output : String || function : reverse the words of the
     * given string and return it
     * 
     * @param sentence
     */
    public static String reverseWordOfSentence(String sentence) {
	String[] sentArray = sentence.split(" ");
	String temp = "";
	for (int i = sentArray.length - 1; i >= 0; i--) {
	    temp = temp + sentArray[i] + " ";
	} // for
	return temp.trim();
    }// reverseWordOfSentence(-)

}// ReverseWordOfSentenceApp
